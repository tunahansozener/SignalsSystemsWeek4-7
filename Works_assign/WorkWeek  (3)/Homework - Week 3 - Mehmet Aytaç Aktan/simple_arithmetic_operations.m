b = 3;

sum_result = a + b;
diff_result = a - b;
prod_result = a * b;
quot_result = a / b;

disp(['Sum: ', num2str(sum_result)]);
disp(['Difference: ', num2str(diff_result)]);
disp(['Product: ', num2str(prod_result)]);
disp(['Quotient: ', num2str(quot_result)]);
