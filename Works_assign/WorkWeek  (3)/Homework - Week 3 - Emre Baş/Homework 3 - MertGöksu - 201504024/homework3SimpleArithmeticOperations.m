% Create a MATLAB script that calculates and displays the sum, 
% difference, product, and quotient of two numbers 10 and 3.

a = 10;
b = 3;

sum = a + b;
difference = a - b;
product = a * b;
quotient = a / b;

disp("Sum: " + sum);
disp("Difference: " + difference);
disp("Product: " + product);
disp("Quotient: " + quotient);